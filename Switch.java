
public class Switch 
{
	private int switchOne;
	private int switchTwo;
	
	public Switch(int switchOne, int switchTwo) 
	{
		this.switchOne = switchOne;
		this.switchTwo = switchTwo;
	}

	public int getSwitchOne() {
		return switchOne;
	}

	public void setSwitchOne(int switchOne) {
		this.switchOne = switchOne;
	}

	public int getSwitchTwo() {
		return switchTwo;
	}

	public void setSwitchTwo(int switchTwo) {
		this.switchTwo = switchTwo;
	}
	
}
